# Write a Python program to calculate the absolute value of a 
# floating point number. 
# Expected Output :
# 2.1                                                                  
# 0.0                                                                  
# 10.1                                                                  
# 0.0 

import math

print(math.fabs(-2.1))
print(math.fabs(-0.0))
print(math.fabs(10.1))
print(math.fabs(0.0))