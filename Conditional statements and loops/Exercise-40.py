# Write a Python program to find the median of three values. Go to the editor
# Expected Output:

# Input first number: 15                                                  
# Input second number: 26                                                 
# Input third number: 29                                                  
# The median is 26.0

p = int(input('Input the 1st number: '))
q = int(input('Input the 2nd number: '))
r = int(input('Input the 3rd number: '))

print('The median is %0.2f'%((p+q+r)/3))